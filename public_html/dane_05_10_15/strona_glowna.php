<br>		 
	<p align='justify'>	<b>N</b>asza firma świadcząca profesjonalny wynajem autokarów na terenie Polski i zagranicy. Jesteśmy firmą z dużym doświadczeniem w branży z przewozem osób i wynajmu autokarów.
		 Nasz profesjonalizm i elastyczne podejście do klienta zaowocowało uznaniem i rzeszą stałych klientów, którzy darzą nas sympatią i zaufaniem.
<br> 
Dysponujemy profesjonalną, rzetelną i zaufaną załogą kierowców, posiadamy wszystkie wymagane zezwolenia i certyfikaty kompetencji zawodowych dotyczących przewozu osób na terenie kraju i zagranicy. Dysponujemy pojazdami z kompletnym i komfortowym wyposażeniem. Pojazdy są w doskonałym stanie technicznym, sa stale serwisowane.
 <br>
Naszą zaletą jest elastyczne podejście do klienta, koszty przejazdów są każdorazowo uzgadniane z klientem, istnieje możliwość negocjacji cen.
